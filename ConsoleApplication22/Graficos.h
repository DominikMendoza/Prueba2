void imprimirE(int x, int y)
{
	for (int i = 1; i <= 7; i++)                      //E
	{
		Console::SetCursorPosition(x, y + i - 1);

		for (int j = 1; j <= 3; j++)
		{
			if (i == 1 || (i == 4 && j != 1))
				cout << char(220) << char(220);
			else if (i == 7)
				cout << char(223) << char(223);
			else if (j == 1)
				cout << char(219);
			else
				cout << "  ";
		}
	}
}

